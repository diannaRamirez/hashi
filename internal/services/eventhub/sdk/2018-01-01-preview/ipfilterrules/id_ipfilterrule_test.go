var _ resourceids.ResourceId = IpfilterruleId{}

func TestNewIpfilterruleID(t *testing.T) {
	id := NewIpfilterruleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "ipFilterRuleValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NamespaceName != "namespaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'NamespaceName'", id.NamespaceName, "namespaceValue")
	}

	if id.IpFilterRuleName != "ipFilterRuleValue" {
		t.Fatalf("Expected %q but got %q for Segment 'IpFilterRuleName'", id.IpFilterRuleName, "ipFilterRuleValue")
	}
}

func TestFormatIpfilterruleID(t *testing.T) {
	actual := NewIpfilterruleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "ipFilterRuleValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.EventHub/namespaces/namespaceValue/ipfilterrules/ipFilterRuleValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", actual, expected)
	}
}

