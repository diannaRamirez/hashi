var _ resourceids.ResourceId = VirtualnetworkruleId{}

func TestNewVirtualnetworkruleID(t *testing.T) {
	id := NewVirtualnetworkruleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "virtualNetworkRuleValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NamespaceName != "namespaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'NamespaceName'", id.NamespaceName, "namespaceValue")
	}

	if id.VirtualNetworkRuleName != "virtualNetworkRuleValue" {
		t.Fatalf("Expected %q but got %q for Segment 'VirtualNetworkRuleName'", id.VirtualNetworkRuleName, "virtualNetworkRuleValue")
	}
}

func TestFormatVirtualnetworkruleID(t *testing.T) {
	actual := NewVirtualnetworkruleID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "virtualNetworkRuleValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.EventHub/namespaces/namespaceValue/virtualnetworkrules/virtualNetworkRuleValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", actual, expected)
	}
}

